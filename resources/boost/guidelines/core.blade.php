**Package:** `bunkuris/has-cache` — MIT License — Copyright (c) 2025 Dāvis Puciriuss

### When to Activate `has-cache`

Activate when implementing or using this package inside a Laravel application:

- Adding `HasCache` trait to a model
- Creating or working with a `CacheKeyManager`
- Using `CacheKey` methods in application code
- Writing tests that assert cache invalidation
- Troubleshooting stale or unexpected cache clearing
